var n1= Number(prompt('digite um numero?'))
var n2= Number(prompt('digite outro numero?'))
var operacao= prompt('digite uma operação?')
if(operacao=="+"){
    alert(n1+n2)
}
else if(operacao=="-"){
alert(n1-n2)
}

else if(operacao=="/"){
    alert(n1/n2)
}

else if(operacao=="*"){
    alert(n1*n2)
}
