var dia = Number(prompt('digite um dia da semana? '))
if(dia==1){
    alert('segunda-feira')
}
else if(dia==2){
    alert('terca-feira')
}
else if(dia==3){
    alert('quarta-feira')
}
else if(dia==4){
    alert('quinta-feira')
}
else if(dia==5){
    alert('sexta-feira')
}
else if(dia==6){
    alert('sabado')
}
else if(dia==7){
    alert('domigo')
}
else{
    alert("erro")
}
